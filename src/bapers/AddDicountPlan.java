    private void addFixedDiscountBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFixedDiscountBtnActionPerformed
        if (fixedPercentageTxt.getText().equals("") || discIdTxt.getText().equals("")){ //no empty values can be passed (doesn't make sense in scenario) and we generate a warnign message
            JOptionPane.showMessageDialog(null, "Enter Discount ID and Discount Rate",  "Add Discount", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                Controller.addFixedDiscount(Integer.parseInt(discIdTxt.getText()),Float.parseFloat(fixedPercentageTxt.getText())); //calls add discount method from controller class with values inputed in gui
            }catch(NumberFormatException ex){ //catching any wrong format issues like usage of Strings where Integers are needed
                JOptionPane.showMessageDialog(null, "Discount Rate and Discount ID must be numbers",  "Add Discount", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_addFixedDiscountBtnActionPerformed
    
