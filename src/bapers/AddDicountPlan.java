    //method for setting band in the discount
    private void createBandBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBandBtnActionPerformed
        if (upperBoundTxt.getText().equals("") || lowerBoundTxt.getText().equals("") || FlexiRateTxt.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Enter Values!",  "Add Discount", JOptionPane.WARNING_MESSAGE); //empty values in gui message 
        }else {
            try{
                float upper = Float.parseFloat(upperBoundTxt.getText());
                float lower = Float.parseFloat(lowerBoundTxt.getText());
                float rate = Float.parseFloat(FlexiRateTxt.getText());
                
                if (upper < lower && upper != 0 ){
                    JOptionPane.showMessageDialog(null, "Invalid Band",  "Add Discount", JOptionPane.WARNING_MESSAGE);
                }else{
                    upperList.add(upper);
                    lowerList.add(lower);
                    ratesLinkedList.add(rate);
                    ratesList.addElement(lower + " " + "-" + " " + upper + " " + "Rate: " + rate);
                }
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Upper, Lower and Discount Rate Must be numbres",  "Add Discount", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_createBandBtnActionPerformed

    private void addFlexibleDiscountBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlexibleDiscountBtnActionPerformed
        if(tasks.isEmpty()){
            JOptionPane.showMessageDialog(null, "Add Tasks First!",  "Add Discount", JOptionPane.WARNING_MESSAGE); //can't add discount without tasks and shows appropriate warning message
        }else if(discIdTxt.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Enter Discount ID!",  "Add Discount", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                Controller.addFlexidisc(tasks, varRates, Integer.parseInt(discIdTxt.getText()));
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Discount ID Must Be A Number",  "Add Discount", JOptionPane.WARNING_MESSAGE);
            }
        } 
    }//GEN-LAST:event_addFlexibleDiscountBtnActionPerformed

