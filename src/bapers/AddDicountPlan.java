    private void getTasks(){
        availableModel.setRowCount(0);
        addedModel.setRowCount(0);
        ResultSet available = Controller.getAllTaskItems();
        if(available != null){
            try{
                while(available.next()){
                    String taskID = available.getString("task_id");
                    String description = available.getString("task_description");
                    availableModel.addRow(new Object [] {taskID, description});
                }
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, "Error Displaying Taks: " + ex.getMessage() ,  "Add Discount", JOptionPane.WARNING_MESSAGE);
            }
        }
    }
    
