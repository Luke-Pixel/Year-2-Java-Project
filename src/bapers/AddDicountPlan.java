    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (flexibleRateTxt.getText().equals("") || taskTxt.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Enter Discount rate and Task ID",  "Add Discount", JOptionPane.WARNING_MESSAGE);
        }else{
            try{
                boolean found = Controller.getTaskById(Integer.parseInt(taskTxt.getText()));
                float rate = Float.parseFloat(flexibleRateTxt.getText());
                boolean added = false;
                if(found == true){
                    for(int i = 0; i < addedModel.getRowCount(); i++){
                        if (addedModel.getValueAt(i, 0).toString().equals(taskTxt.getText())){
                            added = true;
                            JOptionPane.showMessageDialog(null, "Task Already Added To Discount!",  "Add Discount", JOptionPane.WARNING_MESSAGE);
                        }
                    }
                    if (added == false ){
                        String description = "";
                        for(int i = 0; i < availableModel.getRowCount(); i++){
                            if(taskTxt.getText().equals(availableModel.getValueAt(i, 0).toString())){
                                description = availableModel.getValueAt(i, 0).toString();
                            }
                        }
                        addedModel.addRow(new Object [] {taskTxt.getText(), description,flexibleRateTxt.getText()});
                        tasks.add(Integer.parseInt(taskTxt.getText()));
                        varRates.add(rate);
                    }
                }
            }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null, "Discount Rate and Task ID must be numbers",  "Add Discount", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if (taskTxt.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Enter Discount rate and Task ID",  "Set Discount", JOptionPane.WARNING_MESSAGE);
        }else{
             try{
                 boolean added = false;
                 int taskID = Integer.parseInt(taskTxt.getText());
                 for(int i = 0; i < addedModel.getRowCount(); i++){
                        if (addedModel.getValueAt(i, 0).toString().equals(taskTxt.getText())){
                            added = true;
                            addedModel.removeRow(i);
                            for(int p = 0; p < tasks.size(); p++){
                                if(tasks.get(p) == taskID){
                                    tasks.remove(p);
                                    varRates.remove(p);
                                }
                            }
                            
                        }
                    }
                 if(added == true){
                     JOptionPane.showMessageDialog(null, "Task Removed",  "Add Discount", JOptionPane.WARNING_MESSAGE);
                 }else{
                     JOptionPane.showMessageDialog(null, "Task Not Found",  "Add Discount", JOptionPane.WARNING_MESSAGE);
                 }
             }catch(NumberFormatException ex){
                 JOptionPane.showMessageDialog(null, "Task ID Must Be  Number",  "Add Discount", JOptionPane.WARNING_MESSAGE);
             }
         }
    }//GEN-LAST:event_jButton3ActionPerformed
