/**
     * when this method is called the tasks table will be cleared, and then
     * reloaded with the new data inputted into it
     * also closes connections opened with the database 
     */
    public void updateTasksTable() {
        DefaultTableModel tableModel = (DefaultTableModel) tasksTable.getModel();
        ResultSet tasks;

        tasks = Controller.retrieveTasks();
        tableModel.setRowCount(0);
        try {
            while (tasks.next()) {
                String id = tasks.getString("task_id");
                String description = tasks.getString("task_Description");
                String price = tasks.getString("price");
                String location = tasks.getString("location");
                String time = tasks.getString("duration");
                String shelf = tasks.getString("shelfSlot");
                tableModel.addRow(new Object[]{id, description, location, price, time, shelf});
            }
        } catch (SQLException e) {
        } finally {
            Controller.closeConnections();
            updateTasksList();
        }
    }
/**
     * This method will clear the tasks list, then it will reload it with the 
     * new data that has been added to the list
     */
    public void updateTasksList() {
        DefaultListModel newModel = (DefaultListModel) tasksList.getModel();
        ResultSet tasks;
        tasks = Controller.retrieveTasks();
        newModel.setSize(0);
        try {
            int i = 0;
            while (tasks.next()) {
                tasksArray.add(tasks.getString("task_id") + " " + tasks.getString("task_Description"));
                newModel.addElement(tasksArray.get(i));
                i++;
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        } finally {
            Controller.closeConnections();
        }
    }

    /**
     * gets the element clicked on based on the index provided by
     * 'tasksListMouseClicked'
     * 
     * @param index
     */
    private void updateEditFeilds(int index) {
        List<String> task = new LinkedList<String>();
        String[] parts = tasksList.getModel().getElementAt(index).split(" ");
        int id = Integer.parseInt(parts[0]);
        System.out.println(id);
        task = Controller.selectTask(id);

        viewTskIdTxt.setText(task.get(0));
        editDescriptionTxt.setText(task.get(1));
        editPriceTxt.setText(task.get(2));
        timeTxt.setText(task.get(3));
        editLocationTxt.setText(task.get(4));
        shelfTxt.setText(task.get(5));
        oldShelf = shelfTxt.getText();
    }

