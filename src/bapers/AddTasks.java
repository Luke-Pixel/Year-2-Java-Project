/**
 * gets the data inputted to gui
 * if there is no error with this data it will display the "Task Updated Successfully"
 * message and call updateTasksTable & updateTasksList
 * if there is an error with the data "error updating task" will be displayed
 * @param evt 
 */
    private void updateTaskBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTaskBtnActionPerformed
        try{
        boolean result = false;
        result = Controller.editTasks(editDescriptionTxt.getText(), editLocationTxt.getText(), shelfTxt.getText(), Float.parseFloat(editPriceTxt.getText()), Integer.parseInt(timeTxt.getText()), Integer.parseInt(viewTskIdTxt.getText()));
        if (result == true) {
            Controller.UpdateCurrentShelfs(oldShelf, shelfTxt.getText());
            JOptionPane.showMessageDialog(null, "Task Updated Succesfully", "Task Updated", JOptionPane.INFORMATION_MESSAGE);
            
        } else {
            JOptionPane.showMessageDialog(null, "Error Updating Task", "Error", JOptionPane.ERROR_MESSAGE);
        }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Task duration must be a number", "Error", JOptionPane.ERROR_MESSAGE);
        }
        updateTasksTable();
        updateTasksList();
    }//GEN-LAST:event_updateTaskBtnActionPerformed
