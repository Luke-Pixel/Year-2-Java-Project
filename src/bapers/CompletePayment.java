    public void getJobData(int jobNum){
        boolean alreadyAdded = false;
        boolean found = true;
        LinkedList<String> data = Controller.getJobPayment(jobNum);
        if (data == null){
            System.out.println("Job Not Found");
            found = false;
            
        }else{
            //check alrady added 
            
            for(int i = 0; i < addedJobs.size(); i++){
                if (addedJobs.get(i).equals(Integer.toString(jobNum))){
                    alreadyAdded = true;
                    System.out.println("Job Already Added");
                }
            }
            
        }
        if(found == true && alreadyAdded == false){
            addedModel.addRow(new Object [] {data.get(0), data.get(1)});
            addedJobs.add(data.get(0));
            total = total + Float.parseFloat(data.get(1));
            updateTotal();
        }
    }
    
