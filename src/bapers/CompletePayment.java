    private void cashPaymentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cashPaymentBtnActionPerformed
        if(addedJobs.isEmpty()){
            System.out.println("Add Jobs First"); //warns to pay for jobs that exist 
        }else{
        
        
        
        boolean result = Controller.payForJobsCash(custID, total, addedJobs);
        if (result == true){
            JOptionPane.showMessageDialog(null, "Payment Complete",  "Cash Payment", JOptionPane.INFORMATION_MESSAGE);
            Controller.suspendedPayment(custID);
            for(int i = 0; i < addedJobs.size(); i++){
                Controller.removePaymentAlert(addedJobs.get(i)); //pay for remaining jobs
            }
            this.dispose(); //close this
            Controller.openMainMenu(); //return to main menu
        }else{
            JOptionPane.showMessageDialog(null, "Error Completing Payment",  "Cash Payment", JOptionPane.WARNING_MESSAGE);
        }
        }
    }//GEN-LAST:event_cashPaymentBtnActionPerformed

