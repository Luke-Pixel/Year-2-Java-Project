/**
 * uses the ID provided by the user to search the database for
 * that particular customer 
 * @param evt 
 */
    private void selectCustomerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectCustomerBtnActionPerformed
        String custId = idTxt.getText();
        boolean found = Controller.selectCustomer(custId);
        if (found == true) {
            if (path == 1) {
                this.dispose();
                SetDiscount disc = new SetDiscount(custId);
                disc.setVisible(true);
            } else if (path == 2) {
                this.dispose();
                NewJob nj = new NewJob(custId);
                nj.setVisible(true);
            } else if(path == 3){
                this.dispose();
                System.out.println("cust id" + custId);
                CompletePayment cp = new CompletePayment(custId);
                cp.setVisible(true);
            } else if(path ==4){
                this.dispose();
                CreateCustomer cc = new CreateCustomer("Edit", custId);
                cc.setVisible(true);
            }else if(path == 5){
                Controller.upgradeCustomer(custId);
            }else if (path == 6){
                Controller.downgradeCustomer(custId);
            }else if(path == 7){
                Controller.removeDiscCust(custId);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Customer ID Incorrect ", "Incorect ID", JOptionPane.WARNING_MESSAGE);
            idTxt.setText("");
        }

// TODO add your handling code here:
    }//GEN-LAST:event_selectCustomerBtnActionPerformed
