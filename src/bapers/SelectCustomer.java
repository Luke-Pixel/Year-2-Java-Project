/**
 * Clears the current Customers Table and loads a new one, with any updated 
 * records
 * @param i 
 */
    public void updateTable(int i) {
        DefaultTableModel tableModel = (DefaultTableModel) customersTable.getModel();
        ResultSet customers;
        if (i == 1) {
            customers = Controller.viewCustomers();
        } else {
            tableModel.setRowCount(0);
            System.out.println(fNameTxt.getText() + sNameTxt.getText() + postcodeTxt.getText());
            customers = Controller.searchCustomers(fNameTxt.getText(), sNameTxt.getText(), postcodeTxt.getText());
        }
        try {
            while (customers.next()) {
                String id = customers.getString("customer_id");
                String fName = customers.getString("first_name");
                String sName = customers.getString("second_name");
                String state = customers.getString("state");
                String address = customers.getString("address_Line1");            
                String postcode = customers.getString("postcode");
                String valued = customers.getString("valued_customer");
                tableModel.addRow(new Object[]{id, fName, sName,address, postcode, state, valued});
            }
        } catch (SQLException e) {
        } finally {
            Controller.closeConnections();
        }

    }

