    private void generateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateActionPerformed
        //no empty values
        if (custIDTxt.getText().equals("") || date1Txt.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Required Feilds Empty", "Empty Feilds", JOptionPane.WARNING_MESSAGE);
        } else {
            updateTA(); //get latest data
            aa.setText(custIDTxt.getText()); //diplay the current customer who's report is being generated
            bb.setText(date1Txt.getText()); //diplay the current date for the report which is being generated
        }

    }//GEN-LAST:event_generateActionPerformed

